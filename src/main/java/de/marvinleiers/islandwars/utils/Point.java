package de.marvinleiers.islandwars.utils;

public class Point
{
    private double x;
    private double z;

    public Point(double x, double z)
    {
        this.x = x;
        this.z = z;
    }

    public double getX()
    {
        return x;
    }

    public double getZ()
    {
        return z;
    }
}
